describe("Error System", function()
    local errors = require("jade.errors")

    describe("JadeError", function()
        it("creates error with code and message", function()
            local err = errors.JadeError.new("J0001", "Test error", { key = "value" })
            assert.are.equal("J0001", err.code)
            assert.are.equal("Test error", err.message)
            assert.are.equal("value", err.details.key)
        end)

        it("converts to string", function()
            local err = errors.JadeError.new("J0001", "Test error")
            assert.are.equal("[J0001] Test error", tostring(err))
        end)

        it("converts to JSON", function()
            local err = errors.JadeError.new("J0001", "Test error", { key = "value" })
            local json = err:toJSON()
            assert.are.equal("J0001", json.code)
            assert.are.equal("Test error", json.message)
            assert.are.equal("value", json.details.key)
        end)

        it("formats message with details", function()
            local err = errors.JadeError.new("J0001", "Error", { host = "localhost", port = 5432 })
            local msg = err:format("Connection to {host}:{port} failed")
            assert.are.equal("Connection to localhost:5432 failed", msg)
        end)
    end)

    describe("Error codes", function()
        it("has all connection error codes", function()
            assert.are.equal("J0001", errors.AUTHENTICATION_FAILED)
            assert.are.equal("J0002", errors.CONNECTION_REFUSED)
            assert.are.equal("J0003", errors.CONNECTION_TIMEOUT)
            assert.are.equal("J0004", errors.DATABASE_NOT_FOUND)
        end)

        it("has all query error codes", function()
            assert.are.equal("J1000", errors.QUERY_SYNTAX_ERROR)
            assert.are.equal("J1008", errors.NO_ROWS_FOUND)
            assert.are.equal("J1016", errors.UNIQUE_CONSTRAINT_VIOLATION)
        end)

        it("has all migration error codes", function()
            assert.are.equal("J2000", errors.MIGRATION_FAILED)
            assert.are.equal("J2002", errors.MIGRATION_ALREADY_APPLIED)
        end)

        it("has all security error codes", function()
            assert.are.equal("J5000", errors.SQL_INJECTION_DETECTED)
            assert.are.equal("J5001", errors.INVALID_IDENTIFIER)
        end)
    end)

    describe("Error factories", function()
        it("creates ConnectionError", function()
            local err = errors.ConnectionError("J0001", { host = "localhost" })
            assert.is_not_nil(err)
            assert.are.equal("J0001", err.code)
        end)

        it("creates QueryError", function()
            local err = errors.QueryError("J1008", { table = "users" })
            assert.is_not_nil(err)
            assert.are.equal("J1008", err.code)
        end)

        it("creates MigrationError", function()
            local err = errors.MigrationError("J2000", { name = "init" })
            assert.is_not_nil(err)
            assert.are.equal("J2000", err.code)
        end)

        it("creates SecurityError", function()
            local err = errors.SecurityError("J5000", { pattern = "OR" })
            assert.is_not_nil(err)
            assert.are.equal("J5000", err.code)
        end)
    end)

    describe("Error messages", function()
        it("gets message for error code", function()
            local msg = errors.getMessage("J0001", { host = "localhost", user = "admin" })
            assert.is_truth(msg:match("localhost"))
            assert.is_truth(msg:match("admin"))
        end)

        it("returns unknown for invalid code", function()
            local msg = errors.getMessage("J9999")
            assert.is_truth(msg:match("Unknown error"))
        end)
    end)
end)
